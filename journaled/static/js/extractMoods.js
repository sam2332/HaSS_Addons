function extractMoods(text) {
    // Define regex patterns to capture a single word after mood-indicating phrases
        const patterns = [
            /\bIt makes me feel so (\w+)\s/gi,
            /\bI feel so (\w+)\s/gi,
            /\bMakes me feel (\w+)\s/gi,
            /\bI'm feeling so (\w+)\s/gi,
            /\bI am feeling so (\w+)\s/gi,
            /\bIt makes me feel (\w+)\s/gi,
            /\bI'm feeling (\w+)\s/gi,
            /\bI feel (\w+)\s/gi,
            /\bIt makes me (\w+)\s/gi,
            /\bI've been feeling (\w+)\s/gi,
            /\bI am feeling (\w+)\s/gi,
            /\bIm feeling (\w+)\s/gi,
            /\bi'm feeling (\w+)\s/gi,
            /\bI am (\w+)\s/gi,
            /\bI'm (\w+)\s/gi,
            /\bFeeling (\w+)\s/gi,
            /\bThis leaves me (\w+)\s/gi,
            /\bLeft me feeling (\w+)\s/gi,
            /\bI am left feeling (\w+)\s/gi,
            /\bMy mood is (\w+)\s/gi,
            /\bThis makes me (\w+)\s/gi,
            /\bI can't help but feel (\w+)\s/gi,
            /\bI was feeling (\w+)\s/gi
        ];

    // Array to collect all matched moods
    let moods = [];

    // Iterate over each pattern
    for (const pattern of patterns) {
        let match;
        // Use the pattern to capture single-word moods
        while ((match = pattern.exec(text)) !== null) {
            // Add each found mood
            moods.push(match[1]);
        }
    }

    // Handle cases where multiple moods are found
    if (moods.length > 1) {
        // Find if any of the moods are the not in Not_Moods
        moods = moods.filter(mood => !Not_Moods.includes(mood));
        // Return the first mood if any
        return moods.length ? moods[0] : null;

    } else {
        // If only one mood, return it directly
        moods = moods.length ? moods[0] : null;
        if (Not_Moods.includes(moods)) {
            moods = null;
        }
        return moods;
    }


}
